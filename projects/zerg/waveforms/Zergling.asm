Zergling_EndOScan
	LDA	INTIM
	BPL	Zergling_EndOScan

	; End OverScan, so we can calculate better!

Zergling_LoadSample
	LDX	#temp10			; 2
	LDA	($00,x)			; 6 (8)  Load pointer, increase
	CMP	#Zergling_EOF_byte	; 2 (10)
	BEQ	Zergling_FinishHim	; 2 (12)
	INC	0,x			; 6 (18)
	BNE	*+4			; 2 (20) 
	INC	1,x 			; 6 (26) Load pointer, increase

	TAY

	LDA	#1			; 2
	SEC				; 2
	SBC	temp09			; 3
	STA	temp09			; 3
	TAX				; 2
	LDA	Zergling_WaitTimes,x ; 5
	
Zergling_Loop1
	SBC	#1
	BCS	Zergling_Loop1 	

	sleep	3


	TYA				; 2
	STA	AUDV0			; 3 (5)
	LSR				; 2 (7)
	LSR				; 2 (9)
	LSR				; 2 (11)
	LSR				; 2 (13)

	TAY				; 2 (15)

	LDA	Zergling_WaitTimes,x ; 5
Zergling_Loop2
	SBC	#1
	BCS	Zergling_Loop2

	sleep	17

	TYA				; 2
	STA	AUDV0			; 3 (5)
	JMP	Zergling_LoadSample	; 3 (8)
	
Zergling_FinishHim
	LDX	#Zergling_Constant	; 2 (14)

	LDA	#0
	STA	COLUP0
	STA	COLUP1
	STA	COLUPF
	STA	COLUBK
	JMP	Zergling_JumpHereToFake

