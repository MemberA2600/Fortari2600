txt = txt.split("\n")

datas = {}
key    = None
temp   = ""
formats = {}
format = None

inside = {}
all = []

for line in txt:
    if len(line) == 0: continue
    if line.startswith("*"): continue
    if line.startswith("\t") == False:
       if key != None and temp != "":
          datas[key]   = temp
          all.append(key)
          formats[key] = format
       key  = line
       temp = ""
       format = "D"
    elif "BYTE" in line:
        line = line.split(";")[0].split("BYTE")[1]
        while line[0] in ("\t", "#", " "): line = line[1:]

        if "%" in line:
            line = str(int(line.replace("%", "0b"), 2))
            format = "B"

        if "$" in line:
            line = str(int(line.replace("$", "0x"), 16))
            format = "H"

        temp += line

for first in datas:
    for second in datas:
        if first == second: continue
        if datas[second] in datas[first] and second in all:
           if first not in inside.keys():
              inside[first] = []

           inside[first].append(second)
           all.remove(second)

    if first in inside.keys():
       print(first+": " + "\n>>"+ "\n>>".join(inside[first]))
